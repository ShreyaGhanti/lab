//Swapping two variables refers to mutually exchanging the values of thevariables. Generally, this is done with the data in memory

public class Swapping{ 
public static void main(String[]args){
int a=10;
int b=20;
System.out.println("The one variable a is="+a );
System.out.println("The second variable b is="+b );

int temp;
temp=a;
a=b;
b=temp;
System.out.println("The one number after swapping a ="+a );
System.out.println("The second number after swapping b ="+b );
}
}
